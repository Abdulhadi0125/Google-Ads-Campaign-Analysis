CREATE TABLE campaigns (
  report_date DATE,
  campaign_name VARCHAR(150),
  objective VARCHAR(100),
  reach INT,
  impressions INT,
  frequency FLOAT,
  amount_spent FLOAT,
  ctr FLOAT,
  cpc FLOAT,
  cpm FLOAT,
  cost_per_1000_reach FLOAT,
  conversions INT,
  conversion_value FLOAT,
  cost_per_conversion FLOAT,
  post_comments INT,
  link_clicks INT,
  messaging_conversations INT
);
